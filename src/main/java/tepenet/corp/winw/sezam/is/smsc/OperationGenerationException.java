package tepenet.corp.winw.sezam.is.smsc;

public class OperationGenerationException extends Exception {

	private static final long serialVersionUID = -1197929345142595425L;

	public OperationGenerationException() {
		super();
	}

	public OperationGenerationException(String message, Throwable cause) {
		super(message, cause);
	}

	public OperationGenerationException(String message) {
		super(message);
	}

	public OperationGenerationException(Throwable cause) {
		super(cause);
	}
}
