package tepenet.corp.winw.sezam.is.smsc;

public class OperationTypeUnknownException extends Exception {

	private static final long serialVersionUID = 6427507644436634776L;

	public OperationTypeUnknownException() {
		super();
	}

	public OperationTypeUnknownException(String message, Throwable cause) {
		super(message, cause);
	}

	public OperationTypeUnknownException(String message) {
		super(message);
	}

	public OperationTypeUnknownException(Throwable cause) {
		super(cause);
	}
}
