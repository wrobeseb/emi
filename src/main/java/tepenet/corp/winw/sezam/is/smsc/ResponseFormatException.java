package tepenet.corp.winw.sezam.is.smsc;

public class ResponseFormatException extends FormatException {

	private static final long serialVersionUID = 3138810948383560824L;

	public ResponseFormatException() {
		super();
	}

	public ResponseFormatException(String message, Throwable cause) {
		super(message, cause);
	}

	public ResponseFormatException(String message) {
		super(message);
	}

	public ResponseFormatException(Throwable cause) {
		super(cause);
	}
}
